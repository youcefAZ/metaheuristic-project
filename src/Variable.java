public class Variable {
    String x1,x2,x3;

    public Variable(String x1, String x2, String x3) {
        this.x1 = x1;
        this.x2 = x2;
        this.x3 = x3;
    }

    public String getX1() {
        return x1;
    }

    public String getX2() {
        return x2;
    }

    public String getX3() {
        return x3;
    }
}
